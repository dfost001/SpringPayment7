package pp_payment;

/**
 *
 * @author Dinah3
 */
public class Link {
    
    private String href;
    private String rel;
    private String method;
    
    public Link(){}

    public String getHref() {
        return href;
    }

    public void setHref(String href) {
        this.href = href;
    }

    public String getMethod() {
        return method;
    }

    public void setMethod(String method) {
        this.method = method;
    }

    public String getRel() {
        return rel;
    }

    public void setRel(String rel) {
        this.rel = rel;
    }
    
    
    
}
